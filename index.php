      <h3>100 Years Ago</h3>
      <p>For almost 100 years (1859 – 1954), light keepers lived on the island to keep the light burning for mariners on the lake. When the lighthouse lamp was converted to electricity in 1954, a light keeper was no longer needed. The buildings were left neglected and suffered at the hands of vandals. The lighthouse still stood intact, but little was left of the light keeper’s quarters. The roof had fallen in and the walls and floors had crumbled into a pile of rubble in the basement. </p>
    </div>
    <div class="large-3 small-12 columns whiteBox">
      <h3>1997</h3>
      <p>In 1997 the local Marine Heritage Society established specific goals for the island's restoration. The project was to be funded by donations and fundraising efforts, and the majority of the work was to be done by volunteers.
<br><br>The Light Keeper's Cottage restoration was completed in 2001. Over 250 volunteers put in more than 300,000 hours of labor to finish the restoration and ready the boat for tours.</p>
    </div>
    <div class="large-3 small-12 columns whiteBox">
      <h3>Today</h3>
      <p>Today the tour is operated with over 125 volunteers, whose jobs include island housekeepers, gardeners, tour boat captains, crew and tour guides, and gift shop sales and service personnel. <br><br> Volunteers also install the portable walkways and docks every spring and remove them every fall. To date, there have been over 12,000 visitors to the island. </p>
